function createPrefectureSort(prefName: string) {
  return (a: Company, b: Company) => {
    const aLocal = a.address?.includes(prefName) ? 0 : 1;
    const bLocal = b.address?.includes(prefName) ? 0 : 1;
    if (aLocal !== bLocal) return aLocal - bLocal;
    return a.rankPosition - b.rankPosition;
  };
}

